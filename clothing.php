            <h2>Maldivian Attire over the Ages</h2>
            <p>During the 850 years of written Maldivian history, the attire of both men and women has evolved, initially slowly and later more rapidly. Explore the transformations from ancient times to modern periods:</p>

            <h3>Attire During Ibn Batuta's Visit (1354-1355 AD)</h3>
            <p>During this period, men commonly wore a waist wrapper and a garment called "wilyan" around their shoulders. Headwear included turbans or small kerchiefs. Women did not cover their heads and usually wore a waist-wrapper that extended to their knees.</p>
            <!-- Image from Ibn Batuta's descriptions -->

            <h3>During Pyrard de Laval's Visit (Early 17th Century)</h3>
            <p>By the early 17th century, men's attire evolved to include garments like "mundu" and "ganjufaras," while women started wearing "libaas," long dresses with ornamental wide necks.</p>
            <img src="images/man_woman_17th_cent.jpg" alt="Man and woman in the 17th century">
            <p>Around 200 years after the visit of Pyrard not much was changed as seen in the image below of a family from 1885</p>
            <img src="images/family_1885.jpg" alt="Famiyly from 1885">

            <h3>20th Century Changes</h3>
            <p>With exposure to global influences, Maldivian clothing transformed significantly. Men began adopting attire like "aygudhu libaas" and shirts, with trousers becoming compulsory office wear by 1968. Women started wearing "faaskuri hedhun," a modern adaptation.</p>
            <img src="images/faaskuri hedhun.jpg" alt="Faaskuri Hedhun">

            <h3>Introduction of Buruga</h3>
            <p>In the late 20th century, "buruga," a traditional Muslim women's headgear, became prominent. It later evolved into "fashion buruga," accompanied by body-hugging dresses and jeans, reflecting a blend of traditional and modern fashion trends.</p>
