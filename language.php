            <h3>Dhives Akuru</h3>
            <p>Dhives Akuru was used extensively in the Maldives until the end of the 17th century and into the early 18th century. This script was primarily used for official and religious documents. Dhives Akuru represents a significant period in Maldivian history when local governance and literary activities were flourishing. The script's influence is seen in numerous inscriptions and documents from that era.</p>
