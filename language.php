            <p>The Maldivian language, Dhivehi, has a rich history influenced by various cultures over the centuries. It is an Indo-Aryan language spoken by the Maldivian people and uses the unique Thaana script. Dhivehi has evolved through significant cultural and historical changes, reflecting the diverse influences that have shaped the Maldives.</p>

            <h3>Eveyla Akuru</h3>
            <p>Early forms of the Maldivian script, Eveyla Akuru, were found on Buddhist relics dating back to the 6th century AD. These scripts are indicative of the early Buddhist influence in the Maldives. The Eveyla Akuru script was used for inscriptions on coral stone and copper plates. This script further developed into what is known as Dhives Akuru by the 17th century.</p>
