            <h3>Thaana Akuru</h3>
            <p>The Thaana script, modified from existing Thaana script, began to replace Dhives Akuru around 1780 AD. This change was part of a broader cultural shift within the Maldives. Thaana Akuru was created using Arabic numerals as a base, reflecting the Islamic influence after the conversion of the Maldives to Islam in the 12th century. Thaana script is still in use today and remains an integral part of Maldivian identity, utilized in everyday communication, education, and government documentation.</p>
