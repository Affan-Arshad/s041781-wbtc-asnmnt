            <article>
                <h3>Coir Ropes</h3>
                <p>Coir rope making or Roanu Veshun (in Dhivehi) is one of the traditional handicrafts in the Maldives. The ropes from fiber of coconut are an important part of life in the Maldives, because they had a great use. Ropes were widely used by early settlers for the construction of huts and hammocks, and the construction of sailing ships or traditional "fungi" houses. Ancient Maldivians chose coconut fibers, as this is a durable material, as well as an easily accessible product.</p>
                
                <p>People of the northern most atoll of Maldives, Haa Alif atoll is famous for the production of the highly attributed fine coir rope called "aiyvaa roanu". Coconut shells are impregnated for about two to three weeks, keeping them on the beach. Then they are ground and the fibers are separated, which are washed and dried in the sun. Then manually twist and weave into ropes of various sizes. Depending on the desired thickness of the coir rope, few strands (about 6-10) are taken into the palms and the ends of these strands are joined to the ends of another few strands by spinning between the palms. This method is continued to produce a bundle of coir rope of a determined length. The process of weaving coir into rope is locally known as "roanu veshun". The coir ropes or 'Roanu' are now mainly used in ornamental purposes.</p>
