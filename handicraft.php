                <h3>Laajehun</h3>
                <p>Lacquer works in the Maldives is also known as Liye Laajehun and has always been a magnificent art that has produced creative designs that have survived in the history of the Maldives. Since ancient times, the technique has been used to create vases, caskets and kitchen ware of incredible beauty, which were mainly used by the Maldivian royal families.</p>
                <p>This is a very decorative art made by experienced craftsmen, in which lathes are used to paint layers of colored lac over shaped wood. Usually the artist starts with a bright color and ends with a dark color, such as black. Then the artist uses a sharp tip to cut out drawings that reveal the lower layers. At the end, polish is applied and the product is ready for use.</p>
