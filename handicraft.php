                <h3>Kudhi Dhoni</h3>
                <p>Dhoni or sailing boats are the main invention of the Maldivians, as they were the first modes of transport among the islanders. Sailing ships are designed to withstand the power of the oceans and to provide swift motion. Crafting of miniature models of these ancient works of art is known as Dhoni crafting.</p>
                
                <p>In the Maldives, models of different size and quality are available for sale. Maldives craftsmen mainly use wood, creating stunning models, featuring ancient sea vessels. Parts of the boat are polished and varnished before they are embedded in the model.</p>
