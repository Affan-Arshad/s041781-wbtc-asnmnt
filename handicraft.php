                <h3>Wood Carving</h3>
                <p>Due to the remoteness and abundance of trees in the Maldives, the ancient islanders used wood from trees to create products to optimize their work and meet the needs of island life. Items such as kitchen utensils and containers were cut from wood. Products are often polished and can represent great designs.</p>
                
                <p>Wood carving has evolved over the years and is reflected in works of art such as miniature models that show the life and culture of early Maldivian settlers. Wood carving is sometimes painted and sometimes varnished. Wooden figurines and text in wood were also made by early Maldivians. Traditional Maldivian mosques have large wooden boards with the text of the Koran carved into them.</p>
